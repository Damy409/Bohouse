package model;

/**
 * The PriorityLevel enum represents the priority levels that can be assigned to a task.
 * Priority levels are used to indicate the importance or urgency of a task.
 */
public enum PriorityLevel {

    /**
     * Represents a high priority level, indicating a task of high importance or urgency.
     */
    HIGH,

    /**
     * Represents a low priority level, indicating a task of lower importance or urgency.
     */
    LOW;
    
}
